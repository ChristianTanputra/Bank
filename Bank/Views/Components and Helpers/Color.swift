import Foundation
import SwiftUI

extension Color {
    public static var backgroundGray: Color {
        return Color(UIColor(red: 0.95, green: 0.95, blue: 0.95, alpha: 1))
    }
    
    public static var shadowGray: Color {
        return Color(UIColor(red: 0.86, green: 0.86, blue: 0.86, alpha: 1))
    }
}
